
public class Employee
{
	private int eid;
	private String name;
	private int sal;
	public Employee(int eid, String name, int sal)
	{
		//super();
		this.eid = eid;
		this.name = name;
		this.sal = sal;
	}
	public int getEid()
	{
		return eid;
	}
	public String getName()
	{
		return name;
	}
	public int getSal()
	{
		return sal;
	}
	
	
}
